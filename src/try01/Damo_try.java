package try01;

public class Damo_try {
    public static void main(String[] args) {
        // Assignment Operator
        int a =6;
        int b=7*a;
        System.out.println(b);

        // conditional statement And operator;
        System.out.println(64>8 && 49>7);
        System.out.println(64>8 && 49<7);
        System.out.println(64<8 && 49>7);
        System.out.println(64<8 && 49<7);
        // conditional statement Or operator;
        System.out.println(64>8 || 49>7);
        System.out.println(64>8 || 49<7);
        System.out.println(64<8 || 49>7);
        System.out.println(64<8 || 49<7);


    }
}
